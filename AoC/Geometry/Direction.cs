namespace AoC.Geometry;

/// <summary>
/// Represents a direction in a 2D space.
/// </summary>
public enum Direction
{
    /// <summary>
    /// North direction.
    /// </summary>
    Up = 0,

    /// <summary>
    /// South direction.
    /// </summary>
    Down,

    /// <summary>
    /// West direction.
    /// </summary>
    Left,

    /// <summary>
    /// East direction.
    /// </summary>
    Right,

    /// <summary>
    /// North West direction.
    /// </summary>
    TopLeft,

    /// <summary>
    /// North East direction.
    /// </summary>
    TopRight,

    /// <summary>
    /// South West direction.
    /// </summary>
    BottomLeft,

    /// <summary>
    /// South East direction.
    /// </summary>
    BottomRight,
}
